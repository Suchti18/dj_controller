const AsciiBackground = ({ count = 30 }: { count?: number }) => {
    const elementWidth = 500;
    const elementHeight = 500;

    const elements = useMemo(() => {
        return Array.from({ length: count }, (_, i) => {
            const x = Math.random() * (window.innerWidth - elementWidth);
            const y = Math.random() * (window.innerHeight - elementHeight);
            const r = Math.random() * 360;

            const style: React.CSSProperties = {
                left: `${x}px`,
                top: `${y}px`,
                transform: `rotate(${r}deg)`,
            };

            return (
                <div key={i} className="ascii-art" style={style}>
                    {asciiArt}
                </div>
            );
        });
    }, [count]);

    return <>{elements}</>;
};

